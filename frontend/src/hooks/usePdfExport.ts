import { useState, useCallback } from 'react';

export function usePdfExport() {
  const [exporting, setExporting] = useState(false);

  const exportToPdf = useCallback((elementId: string, filename: string) => {
    const el = document.getElementById(elementId);
    if (!el) return;

    setExporting(true);

    // Collect ALL compiled CSS from the current page (Tailwind + custom)
    const styleContent = Array.from(document.styleSheets).reduce((acc, sheet) => {
      try {
        return acc + Array.from(sheet.cssRules).map(r => r.cssText).join('\n');
      } catch {
        return acc;
      }
    }, '');

    const reportTitle = filename.replace(/-/g, ' ').replace('.pdf', '');
    const dateStr = new Date().toLocaleDateString('en-IN', {
      day: 'numeric', month: 'long', year: 'numeric'
    });

    const win = window.open('', '_blank', 'width=960,height=800');
    if (!win) {
      alert('Please allow pop-ups for this site to download the PDF report.');
      setExporting(false);
      return;
    }

    win.document.write(`<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <title>${reportTitle}</title>
  <style>${styleContent}</style>
  <style>
    @page {
      size: A4 portrait;
      margin: 12mm 10mm 10mm 10mm;
    }
    * {
      -webkit-print-color-adjust: exact !important;
      print-color-adjust: exact !important;
      color-adjust: exact !important;
    }
    html, body {
      background: white !important;
      font-family: -apple-system, BlinkMacSystemFont, 'Segoe UI', Roboto, sans-serif;
      margin: 0;
      padding: 0;
      font-size: 13px;
    }
    .pdf-report-header {
      display: flex;
      justify-content: space-between;
      align-items: center;
      padding: 0 0 6px 0;
      margin-bottom: 14px;
      border-bottom: 1.5px solid #e5e7eb;
      font-size: 10px;
      color: #6b7280;
    }
    .pdf-report-header strong {
      font-size: 12px;
      color: #111827;
    }
    .pdf-report-footer {
      margin-top: 16px;
      padding-top: 6px;
      border-top: 1px solid #e5e7eb;
      font-size: 9px;
      color: #9ca3af;
      text-align: center;
    }
    /* Hide interactive controls — not needed in print */
    button, input, textarea, select, [role="button"] { display: none !important; }
    .no-print { display: none !important; }
    /* Avoid page breaks inside cards */
    .card { break-inside: avoid; page-break-inside: avoid; }
  </style>
</head>
<body>
  <div class="pdf-report-header">
    <div>
      <strong>WB Election 2026 — AI Prediction Report</strong>
    </div>
    <div>${dateStr}</div>
  </div>
  ${el.innerHTML}
  <div class="pdf-report-footer">
    Generated by WB Election 2026 Prediction System &nbsp;·&nbsp; For research and analytical purposes only
  </div>
  <script>
    window.onload = function () {
      setTimeout(function () {
        window.focus();
        window.print();
        window.onafterprint = function () { window.close(); };
      }, 400);
    };
  </script>
</body>
</html>`);

    win.document.close();

    // Reset button state after a moment (print dialog is async)
    setTimeout(() => setExporting(false), 1500);
  }, []);

  return { exportToPdf, exporting };
}
