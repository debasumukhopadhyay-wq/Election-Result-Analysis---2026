const seed = (n) => {
  const x = Math.sin(n + 1) * 10000;
  return x - Math.floor(x);
};

const seededRandInt = (n, min, max) => Math.floor(seed(n) * (max - min + 1)) + min;
const seededRandFloat = (n, min, max) => parseFloat((seed(n) * (max - min) + min).toFixed(2));

