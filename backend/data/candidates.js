const getFullName = (n, party, isMuslimMajority) => {
  const useMuslimName = isMuslimMajority && (party === "ISF" || party === "INC" || (party === "TMC" && seed(n * 97) > 0.5));
  if (useMuslimName || (party === "ISF" && seed(n * 101) > 0.2)) {
    const fn = MUSLIM_FIRST[seededRandInt(n * 103, 0, MUSLIM_FIRST.length - 1)];
    const ln = MUSLIM_LAST[seededRandInt(n * 107, 0, MUSLIM_LAST.length - 1)];
    return `${fn} ${ln}`;
  }
  const isFemale = seed(n * 113) > 0.75;
  const fnPool = isFemale ? FIRST_NAMES.F : FIRST_NAMES.M;
  const fn = fnPool[seededRandInt(n * 109, 0, fnPool.length - 1)];
  const ln = LAST_NAMES[seededRandInt(n * 111, 0, LAST_NAMES.length - 1)];
  return `${fn} ${ln}`;
};

// Muslim-majority districts
const MUSLIM_MAJORITY_DISTRICTS = ["Murshidabad", "Uttar Dinajpur", "Malda"];

// District party tendencies for determining whether ISF or INC contests as 4th candidate
// ISF only contests in South 24 Parganas (and a handful of North 24 Parganas seats) in 2026
const ISF_DISTRICTS = ["South 24 Parganas"];

// Generate candidates for a single constituency
const generateCandidates = (constId, constNum, district) => {
  const isMuslimMajority = MUSLIM_MAJORITY_DISTRICTS.includes(district);
  const useISF = ISF_DISTRICTS.includes(district) && seed(constNum * 137) > 0.55;
  const fourthParty = useISF ? "ISF" : "INC";

  const parties = ["TMC", "BJP", "CPM", fourthParty];
  const candidates = [];

  parties.forEach((party, pIdx) => {
    const baseN = constNum * 200 + pIdx * 50;
    const name = getFullName(baseN, party, isMuslimMajority);

    const age = seededRandInt(baseN + 1, 28, 72);
    const eduIdx = seededRandInt(baseN + 2, 0, 3);
    const education = EDUCATIONS[eduIdx];
    const criminalCases = seededRandInt(baseN + 3, 0, 5);
    const isLocalResident = seed(baseN + 4) > 0.25;

    // Incumbents are more likely for the winning party of 2021
    // We use a simple heuristic: TMC likely incumbent in most seats
    let termCount = 0;
    if (party === "TMC") {
      termCount = seededRandInt(baseN + 5, 1, 4);
    } else if (party === "BJP") {
      termCount = seededRandInt(baseN + 5, 0, 2);
    } else {
      termCount = seededRandInt(baseN + 5, 0, 3);
    }

    // Party-based popularity adjustments
    let popularityBase, developmentBase, oratoryBase;
    if (party === "TMC") {
      popularityBase = seededRandFloat(baseN + 6, 5.0, 8.5);
      developmentBase = seededRandFloat(baseN + 7, 5.0, 8.0);
      oratoryBase = seededRandFloat(baseN + 8, 5.0, 8.5);
    } else if (party === "BJP") {
      popularityBase = seededRandFloat(baseN + 6, 4.5, 8.0);
      developmentBase = seededRandFloat(baseN + 7, 4.0, 7.5);
      oratoryBase = seededRandFloat(baseN + 8, 4.5, 8.0);
    } else {
      popularityBase = seededRandFloat(baseN + 6, 3.5, 7.0);
      developmentBase = seededRandFloat(baseN + 7, 3.5, 7.0);
      oratoryBase = seededRandFloat(baseN + 8, 3.5, 7.5);
    }

    const antiIncumbencyRisk = party === "TMC"
      ? parseFloat((0.25 + seed(baseN + 9) * 0.45).toFixed(2))
      : parseFloat((0.05 + seed(baseN + 9) * 0.30).toFixed(2));

    const demographicAlignmentScore = parseFloat((0.30 + seed(baseN + 10) * 0.65).toFixed(2));

    candidates.push({
      id: `CAND-${String(constNum).padStart(3, "0")}-${party}`,
      constituencyId: constId,
      name,
      party,
      age,
      education,
      criminalCases,
      isLocalResident,
      termCount,
      oratoryScore: oratoryBase,
      popularityIndex: popularityBase,
      developmentScore: developmentBase,
      antiIncumbencyRisk,
      demographicAlignmentScore,
    });
  });

  // Occasionally add an Independent candidate
  if (seed(constNum * 157) > 0.60) {
    const baseN = constNum * 200 + 4 * 50;
    candidates.push({
      id: `CAND-${String(constNum).padStart(3, "0")}-IND`,
      constituencyId: constId,
      name: getFullName(baseN, "IND", false),
      party: "IND",
      age: seededRandInt(baseN + 1, 30, 65),
      education: EDUCATIONS[seededRandInt(baseN + 2, 0, 2)],
      criminalCases: seededRandInt(baseN + 3, 0, 3),
      isLocalResident: true,
      termCount: seededRandInt(baseN + 5, 0, 1),
      oratoryScore: seededRandFloat(baseN + 8, 2.5, 6.0),
      popularityIndex: seededRandFloat(baseN + 6, 2.0, 5.5),
      developmentScore: seededRandFloat(baseN + 7, 2.0, 5.5),
      antiIncumbencyRisk: parseFloat((0.10 + seed(baseN + 9) * 0.25).toFixed(2)),
      demographicAlignmentScore: parseFloat((0.20 + seed(baseN + 10) * 0.40).toFixed(2)),
    });
  }

  return candidates;
};

